class Solution {
  public:
    // Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
       vector<int>val(1000010 , 0);
       int n = 0;
       while(head!=nullptr){
           val[head->data]++;n++;
           head = head->next;
       }
       Node*curr = new Node(0);
       Node* result = curr;
       for(int i=0;i<=1000000;i++){
           while(val[i]>0){
               val[i]--;n--;
               curr->next = new Node(i);
               curr = curr->next;
           }
           if(n==0)break;
       }
       return result->next;
    }
};


